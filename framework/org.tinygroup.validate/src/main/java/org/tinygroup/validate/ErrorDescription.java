package org.tinygroup.validate;

/**
 * 错误信息描述
 * @author luoguo
 *
 */
public final class ErrorDescription {
	private String fieldName;// 字段域
	private String description;// 错误信息

	public ErrorDescription(String name, String errorInfo) {
		this.fieldName = name;
		this.description = errorInfo;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String toString() {
		return String.format("%s", description);
	}
}
