package org.tinygroup.service.test.validate;

public class ValidateUser {
	private String name;
	private int age;
	private int grade;
	private ValidateUser mate;
	private ValidateAddress address;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public ValidateUser getMate() {
		return mate;
	}
	public void setMate(ValidateUser mate) {
		this.mate = mate;
	}
	public ValidateAddress getAddress() {
		return address;
	}
	public void setAddress(ValidateAddress address) {
		this.address = address;
	}
	
}
